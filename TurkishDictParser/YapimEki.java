public class YapimEki {

    String ek;
    int action;

    public YapimEki() {
    }

    public YapimEki(String ek, int action) {

        this.ek = ek;
        this.action = action;
    }

    public String getEk() {

        return ek;
    }

    public void setEk(String ek) {
        this.ek = ek;
    }

    public int getAction() {
        return action;
    }

    public void setAction(int action) {
        this.action = action;
    }



}
